# 3. В массиве случайных целых чисел поменять местами минимальный и максимальный элементы.
# [64, 33, 96, 37, 38, 12, 45, 14, 47, 80, 49, 13, 79, 22, 25, 27, 93, 31]
array = [64, 33, 96, 37, 38, 12, 45, 14, 47, 80, 49, 13, 79, 22, 25, 27, 93, 11]
_min = [array[0], 0]
_max = [array[0], 0]

for i, val in enumerate(array):
    if val < _min[0]:
        _min = [val, i]
    if val > _max[0]:
        _max = [val, i]

array[_min[1]] = _max[0]
array[_max[1]] = _min[0]

print(array)